package com.zxj.hashmap;

public class Node<K,V> implements Map.Entry<K,V> {

    public K key;
    public V value;

    // 单项链表
    public Node<K,V> next;

    public Node(K key, V value, Node<K, V> next) {
        this.key = key;
        this.value = value;
        this.next = next;
    }

    @Override
    public K getKey() {
        return key;
    }

    @Override
    public V getValue() {
        return value;
    }

    @Override
    public V setValue(V value) {
        V oldValue = this.value;
        this.value = value;
        return oldValue;
    }
}
